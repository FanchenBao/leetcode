class Solution:
    def shortestPathBinaryMatrix(self, grid) -> int:
        if grid[0][0] == 1:  # edge case
            return -1

        end = len(grid) - 1
        for i in range(end + 1):  # set all blocks to -1
            for j in range(end + 1):
                if grid[i][j] == 1:
                    grid[i][j] = -1
        grid[0][0] = 1

        toVisit: Deque[Tuple[int, int]] = deque()
        toVisit.append((0, 0))  # each node follows the format of (row, col)
        while toVisit:  # Breadth first search
            i, j = toVisit.popleft()
            # push all possible next steps to the queue
            for m in range(i - 1, i + 2):
                for n in range(j - 1, j + 2):
                    if (
                        0 <= m <= end and 0 <= n <= end and grid[m][n] == 0
                    ):  # use grid[m][n] == 0 to check whether a node has been visited already
                        grid[m][n] = 1 + grid[i][j]
                        if m == end and n == end:  # already reaches the end
                            return grid[end][end]
                        else:
                            toVisit.append((m, n))
        return -1


