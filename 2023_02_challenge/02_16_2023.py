class Solution:
    def maxDepth(self, root: Optional[TreeNode]) -> int:
        """LeetCode 104

        39 ms, faster than 88.60%
        """
        if not root:
            return 0
        return max(self.maxDepth(root.left), self.maxDepth(root.right)) + 1


