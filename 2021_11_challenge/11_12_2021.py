from typing import List


# Definition for singly-linked list.
class ListNode:
    def __init__(self, val=0, next=None):
        self.val = val
        self.next = next


class Solution:
    def removeElements(self, head: Optional[ListNode], val: int) -> Optional[ListNode]:
        """LeetCode 203

        O(N), 68 ms, 78% ranking.
        """
        dummy = ListNode(next=head)
        pre, cur = dummy, head
        while cur:
            cur_val = cur.val
            cur = cur.next
            if cur_val == val:
                pre.next = cur
            else:
                pre = pre.next
        return dummy.next


        


