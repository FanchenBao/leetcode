from typing import List


# Definition for a binary tree node.
class TreeNode:
    def __init__(self, val=0, left=None, right=None):
        self.val = val
        self.left = left
        self.right = right


class Solution:
    def trimBST(self, root: Optional[TreeNode], low: int, high: int) -> Optional[TreeNode]:
        """LeetCode 669

        Straightforward solution. If the root value is outside the range, we
        definitely discard the root and the branch where the value is going to
        be even more outside the range. We return the branch that might have
        some value within the range.

        If the root value is inside the range, we must keep it. Thus, we have
        to check both its branches.

        O(N), 49 ms, 89.77% 
        """
        if not root:
            return None
        if root.val < low:
            return self.trimBST(root.right, low, high)
        if root.val > high:
            return self.trimBST(root.left, low, high)
        root.left = self.trimBST(root.left, low, high)
        root.right = self.trimBST(root.right, low, high)
        return root


