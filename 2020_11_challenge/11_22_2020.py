from typing import List


class Solution:
    def uniqueMorseRepresentations(self, words: List[str]) -> int:
        """97% ranking. This is a very easy problem"""
        table = [
            ".-",
            "-...",
            "-.-.",
            "-..",
            ".",
            "..-.",
            "--.",
            "....",
            "..",
            ".---",
            "-.-",
            ".-..",
            "--",
            "-.",
            "---",
            ".--.",
            "--.-",
            ".-.",
            "...",
            "-",
            "..-",
            "...-",
            ".--",
            "-..-",
            "-.--",
            "--.."
        ]
        return len(set(''.join(table[ord(w) - 97] for w in word) for word in words))



