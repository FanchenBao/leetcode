from typing import List


# Definition for singly-linked list.
class ListNode:
    def __init__(self, val=0, next=None):
        self.val = val
        self.next = next


class Solution:
    def getDecimalValue(self, head: ListNode) -> int:
        """LeetCode 1290

        29 ms, 68% ranking.
        """
        res, node = 0, head
        while node:
            res = (res << 1) | node.val
            node = node.next
        return res
        


