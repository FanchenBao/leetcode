class Solution {
    public int getCommon(int[] nums1, int[] nums2) {
        /*
        LeetCode 2540
        
        Merge sort without the merge.

        O(N + M) 2 ms, faster than 81.56%
        */
        int i = 0;
        int j = 0;
        while (i < nums1.length && j < nums2.length) {
            if (nums1[i] == nums2[j]) {
                return nums1[i];
            } else if (nums1[i] > nums2[j]) {
                j++;
            } else {
                i++;
            }
        }
        return -1;
    }
}


