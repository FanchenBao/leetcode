class Solution {
    public int findCenter(int[][] edges) {
        /*
         * LeetCode 1791
         *
         * Mind twister.
         */
        if (edges[1][0] == edges[0][0] || edges[1][0] == edges[0][1])        
            return edges[1][0];
        return edges[1][1];
    }
}


