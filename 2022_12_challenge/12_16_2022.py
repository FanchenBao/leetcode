from typing import List
import math


class MyQueue:

    def __init__(self):
        """LeetCode 232

        pop and peek are not O(1), but amortized O(1)

        55 ms, faster than 48.87%
        """
        self.s1 = []
        self.s2 = []

    def push(self, x: int) -> None:
        self.s1.append(x)

    def pop(self) -> int:
        self.peek()
        return self.s2.pop()

    def peek(self) -> int:
        if not self.s2:
            while self.s1:
                self.s2.append(self.s1.pop())
        return self.s2[-1]

    def empty(self) -> bool:
        return len(self.s1) + len(self.s2) == 0


